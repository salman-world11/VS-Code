public class Assignment {
    public static void main(String[] args) {
        //Assignment operator
        int m1,m2,m3,m4;
        m1=m2=m3=m4=10;
        System.out.println(m1+" " +m2+" "+m3+" "+m4);
        ///compound operator
        int a = 10;
        a +=5;
        System.out.println(a);

        int b = 10;
        b -=5;
        System.out.println(b);

        int d = 10;
        d /=5;
        System.out.println(d);

        int e = 10;
        e *=5;
        System.out.println(e);
    }
}
