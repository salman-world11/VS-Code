public class logical {
    public static void main(String[] args) {
        
        ///logical operator  
        /// input value enter

        int a=10;
        int b=20;

        /// use AND operator (&&)

        System.out.println(a==b && a<=b && a>=b && a>b && a<b);

        /// use OR operator (||)

        System.out.println(a==b || a<=b || a>=b || a>b || a<b);

        // not operator !=

        System.out.println(a==b != a<=b != a>=b != a>b != a<b);
    }
}
